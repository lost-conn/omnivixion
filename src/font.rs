//! Built-in 5×7 voxel bitmap font.
//!
//! Each glyph is a `[u8; 7]` of row bitmasks. Bits are read left-to-right:
//! bit 4 = leftmost pixel (rx=0), bit 0 = rightmost (rx=4).
//! Row 0 is the top of the glyph; emulator code flips ry → world Y.
//!
//! v0.1 charset: `' '`, `'!'`, `'.'`, `':'`, `'0'..'9'`, `'A'..'Z'`. Lowercase
//! falls back to uppercase. Anything else renders as a solid block.

pub const FONT_WIDTH: u8 = 5;     // glyph cells in x
pub const FONT_HEIGHT: u8 = 7;    // glyph cells in y
pub const FONT_ADVANCE: u8 = 6;   // x-advance per glyph, including 1-cell gap

/// Solid 5×7 block; used for unmapped chars so the gap is obvious.
const FALLBACK: [u8; 7] = [
    0b11111,
    0b11111,
    0b11111,
    0b11111,
    0b11111,
    0b11111,
    0b11111,
];

