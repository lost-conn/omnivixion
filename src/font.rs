/// Look up the 7-row bitmask for a character. Returns FALLBACK for any code
/// point outside printable ASCII (0x20..=0x7E).
pub fn glyph(c: char) -> [u8; 7] {
    let i = c as u32;
    if (0x20..=0x7E).contains(&i) {
        FONT_DATA[(i - 0x20) as usize]
    } else {
        FALLBACK
    }
}
