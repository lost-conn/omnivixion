//! Built-in 5×7 voxel bitmap font.
//!
//! Each glyph is a `[u8; 7]` of row bitmasks. Bits are read left-to-right:
//! bit 4 = leftmost pixel (rx=0), bit 0 = rightmost (rx=4).
//! Row 0 is the top of the glyph; the renderer flips ry → world Y.
//!
//! Table is indexed by ASCII offset from 0x20. Anything outside `0x20..=0x7E`
//! falls back to a solid block so missing glyphs are obvious.

pub const FONT_WIDTH: u8 = 5;     // glyph cells in x
pub const FONT_HEIGHT: u8 = 7;    // glyph cells in y
pub const FONT_ADVANCE: u8 = 6;   // x-advance per glyph (5-cell glyph + 1-cell gap)

